@Setter
@AllArgsConstructor
@NoArgsConstructor
public class DepartmentDto {

    private UUID id;
    private String name;

    public DepartmentDto(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
